package exercícios_2021_06_08_Java;

public class Exercício_01_Cliente {

	//atributos
	private String nomeCliente;
	private String profissao;
	private int idade;
	private int numeroCPF;
	private int telefoneContato;
	
	//verde = métodos
		public String getNomeCliente() {
		return nomeCliente;
	}
	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}
	public String getProfissao() {
		return profissao;
	}
	public void setProfissao(String profissao) {
		this.profissao = profissao;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getNumeroCPF() {
		return numeroCPF;
	}
	public void setNumeroCPF(int numeroCPF) {
		this.numeroCPF = numeroCPF;
	}
	public int getTelefoneContato() {
		return telefoneContato;
	}
	public void setTelefoneContato(int telefoneContato) {
		this.telefoneContato = telefoneContato;
	}

}

	
	

